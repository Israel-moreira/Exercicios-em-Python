def dobro(n):
    resultado = n * 2
    return resultado

def metade(n):
    resultado = n / 2
    return resultado

def aumentar(n, p):
    perc = n * (p * 0.01)
    resultado = n + perc
    return resultado

def diminuir(n, p):
    perc = n * (p * 0.01)
    resultado = n - perc
    return resultado
